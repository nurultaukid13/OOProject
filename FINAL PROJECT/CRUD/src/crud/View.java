    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {                                      
        //simpan data
        String jk = null;
        
        //inialisasi untuk pada db di radio button saat pemilihan jenis kelamin
        if(rLaki.isSelected()){
            jk = "Laki-laki";
        }else if (rPerempuan.isSelected()){
            jk = "Perempuan";
        }
        
        //proses pengisian data pada tabel
        try {
            String sql = "INSERT INTO tb_mahasiswa VALUES ('" + tNpm.getText() + "','" 
                    + tNama.getText() + "', '" + jk + "','" + tAlamat.getText() + "')";
            Connection conn = (Connection) Koneksi.getConnection();
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            
            JOptionPane.showMessageDialog(null, "Berhasil Menyimpan");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal Menyimpan" + e.getMessage());
        }   
    }                                     

